package com.ghsh.code.bean;

public class TCommentBean {
	
	private int allCommentNumber;//所有评论数量
	
	private int goodsCommentNumber;//好评数量
	
	private int middleCommentNumber;//中评数量
	
	private int spreadCommentNumber;//差评数量
	
	public int getAllCommentNumber() {
		return allCommentNumber;
	}
	public void setAllCommentNumber(int allCommentNumber) {
		this.allCommentNumber = allCommentNumber;
	}
	public int getGoodsCommentNumber() {
		return goodsCommentNumber;
	}
	public void setGoodsCommentNumber(int goodsCommentNumber) {
		this.goodsCommentNumber = goodsCommentNumber;
	}
	public int getMiddleCommentNumber() {
		return middleCommentNumber;
	}
	public void setMiddleCommentNumber(int middleCommentNumber) {
		this.middleCommentNumber = middleCommentNumber;
	}
	public int getSpreadCommentNumber() {
		return spreadCommentNumber;
	}
	public void setSpreadCommentNumber(int spreadCommentNumber) {
		this.spreadCommentNumber = spreadCommentNumber;
	}
	
}
